# Q1. Explain the concept of variance in your own words. what does variance tell us about dataset
# and why is it important in data analysis and machine learning?
# 
# Ans:
# Variance measures how far the data values are spread from the mean.
# Low variance tell us data points are concentrated at one place neat to mean
# High variance tell us data points are spread wider from the mean
# its important because it help us to understand data patterns, clean data and build better ML models.